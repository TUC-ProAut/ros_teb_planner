function [out] = rosmsg_check(name_part)
% returns or prints all ros messages containing <name_part>

    if (nargin < 1)
        name_part = 'teb_planner_pa_msgs';
    end

    full_list = rosmsg("list");
    ind = find(contains(full_list, name_part));

    if (nargout > 0)
        out = full_list(ind);
    else
        if (~isempty(ind))
            fprintf(['rosmsg containing "', name_part , '":\n']);
            fprintf('  %s\n',full_list{ind});
        else
            warning(['found no rosmsg containing "', name_part , '".']);
        end
    end
end

